def delete_spawns(map):
    nm = []
    for y in range(len(map)):
        m = []
        for x in range(len(map[y])):
            if map[y][x] == 9:
                m.append(0)
            else:
                m.append(map[y][x])
        nm.append(m)
    return nm

def get_spawn_poses_to_map(map, tile_size=16):
    spawn_poses = []
    for y in range(len(map)):
        for x in range(len(map[y])):
            if map[y][x] == 9:
                spawn_poses.append([x*tile_size, y*tile_size])
    
    return spawn_poses